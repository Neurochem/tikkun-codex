function setup() {
  let canvas = createCanvas(720, 270);
  canvas.parent('matrix-canvas');
  textAlign(CENTER, CENTER);
  textSize(14);
}

function draw() {
  background(0);
  stroke(255);
  noFill();
  for (let r = 0; r < 9; r++) {
    for (let c = 0; c < 24; c++) {
      let x = c * 30 + 15;
      let y = r * 30 + 15;
      rect(c * 30, r * 30, 30, 30);
      fill(255);
      text(matrix[r][c], x, y);
      noFill();
    }
  }
  // Highlight Astaroth
  stroke(255, 0, 0);
  rect(7*30, 0, 90, 30);
  fill(255, 0, 0);
  text("Astaroth (ח נ ה)", 7*30 + 45, 25);
}